package main;



public class Mathrandom {

	public static void main(String[] args) {
		
		
			System.out.println(Math.sin(Math.random()));
			
			System.out.println((int)(Math.random()*6));
			
			
			//Valor random entre M y N con M menor que N // M = 3 N = 5
			
			System.out.println((int)((Math.random()*2))+3);
			
			double random = (Math.random()*5);
			
			System.out.println(Math.floor(random));
			
			System.out.println(Math.ceil(random));	
			
	}
	
	

}
